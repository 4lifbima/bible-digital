// Get daily verse based on current date
export function getDailyVerse() {
    const today = new Date()
    const dayOfYear = Math.floor((today - new Date(today.getFullYear(), 0, 0)) / (1000 * 60 * 60 * 24))
    const index = dayOfYear % dailyVerses.length
    return dailyVerses[index]
}

// Search verses
export function searchVerses(query) {
    const results = []
    const lowerQuery = query.toLowerCase()

    const searchInTestament = (testament) => {
        testament.books.forEach(book => {
            book.chapters.forEach(chapter => {
                chapter.verses.forEach(verse => {
                    if (verse.text.toLowerCase().includes(lowerQuery)) {
                        results.push({
                            id: `${book.id}-${chapter.number}-${verse.number}`,
                            book: book.name,
                            bookId: book.id,
                            chapter: chapter.number,
                            verse: verse.number,
                            text: verse.text
                        })
                    }
                })
            })
        })
    }

    searchInTestament(bibleData.perjanjianLama)
    searchInTestament(bibleData.perjanjianBaru)

    return results.slice(0, 20) // Limit to 20 results
}

// Get all books list
export function getAllBooks() {
    return [
        ...bibleData.perjanjianLama.books.map(b => ({ ...b, testament: 'Perjanjian Lama' })),
        ...bibleData.perjanjianBaru.books.map(b => ({ ...b, testament: 'Perjanjian Baru' }))
    ]
}
